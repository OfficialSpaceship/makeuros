def install_package(pkg):
    print(f"Resolving the best installation path for: {pkg}...")
    import shutil
    import subprocess

    # 1. Custom overrides for tricky applications
    if pkg.lower() == "ollama":
        print("Detected 'ollama'. Checking installation options...")
        if shutil.which("yay"):
            print("Installing ollama via 'yay' (AUR)...")
            res = os.system("yay -S --noconfirm ollama")
            if res == 0:
                print("ollama installed successfully via yay!")
                return
        if shutil.which("curl"):
            print("Installing ollama via official curl installer script...")
            res = os.system("curl -fsSL https://ollama.com/install.sh | sh")
            if res == 0:
                print("ollama installed successfully via curl script!")
                return

    # 2. Check official Arch repositories (pacman)
    if shutil.which("pacman"):
        check_pacman = subprocess.run(["pacman", "-Si", pkg], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        if check_pacman.returncode == 0:
            print(f"Package '{pkg}' found in official repositories. Installing via pacman...")
            res = os.system(f"sudo pacman -S --noconfirm {pkg}")
            if res == 0:
                print(f"Successfully installed {pkg} via pacman!")
                return

    # 3. Check AUR (yay)
    if shutil.which("yay"):
        print(f"Checking if '{pkg}' is available in the AUR...")
        check_aur = subprocess.run(["yay", "-Si", pkg], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        if check_aur.returncode == 0:
            print(f"Package '{pkg}' found in AUR. Installing via yay...")
            res = os.system(f"yay -S --noconfirm {pkg}")
            if res == 0:
                print(f"Successfully installed {pkg} via yay!")
                return

    # 4. Check Flatpak
    if shutil.which("flatpak"):
        print(f"Checking if '{pkg}' is available on Flathub...")
        check_flatpak = subprocess.run(["flatpak", "search", pkg], stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True)
        if check_flatpak.returncode == 0 and pkg.lower() in check_flatpak.stdout.lower():
            app_id = ""
            for line in check_flatpak.stdout.split("\n"):
                parts = [p.strip() for p in line.split("\t") if p.strip()]
                if len(parts) >= 2 and (pkg.lower() in parts[0].lower() or pkg.lower() in parts[1].lower()):
                    app_id = parts[1]
                    break
            if app_id:
                print(f"Package '{pkg}' found on Flathub (ID: {app_id}). Installing via Flatpak...")
                res = os.system(f"flatpak install -y flathub {app_id}")
                if res == 0:
                    print(f"Successfully installed {pkg} via Flatpak!")
                    return

    # 5. Check Snap
    if shutil.which("snap"):
        print(f"Checking if '{pkg}' is available on Snap Store...")
        check_snap = subprocess.run(["snap", "info", pkg], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        if check_snap.returncode == 0:
            print(f"Package '{pkg}' found on Snap Store. Installing via snap...")
            res = os.system(f"sudo snap install {pkg}")
            if res == 0:
                print(f"Successfully installed {pkg} via Snap!")
                return

    # 6. Check pip
    if shutil.which("pip"):
        print(f"Checking if '{pkg}' is available on PyPI (pip)...")
        check_pip_dry = subprocess.run(["pip", "install", pkg, "--dry-run"], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        if check_pip_dry.returncode == 0:
            print(f"Package '{pkg}' found on PyPI. Installing via pip...")
            res = os.system(f"pip install --user {pkg}")
            if res == 0:
                print(f"Successfully installed {pkg} via pip!")
                return

    # 7. Known developer install scripts
    common_scripts = {
        "rustup": "curl --proto '=https' --tlsv1.2 -sSf https://sh.rustup.rs | sh",
        "bun": "curl -fsSL https://bun.sh/install | sh",
        "deno": "curl -fsSL https://deno.land/install.sh | sh",
        "nvm": "curl -o- https://raw.githubusercontent.com/nvm-sh/nvm/v0.39.7/install.sh | bash",
        "pipx": "python3 -m pip install --user pipx && python3 -m pipx ensurepath"
    }

    if pkg.lower() in common_scripts:
        script = common_scripts[pkg.lower()]
        print(f"Found developer install script for '{pkg}'. Running installer...")
        res = os.system(script)
        if res == 0:
            print(f"Successfully installed {pkg}!")
            return

    print(f"Error: Could not find a suitable installation method for '{pkg}'. Try installing it manually.")
    sys.exit(1)

