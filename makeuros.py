def main():
    parser = argparse.ArgumentParser(description="makeuros: Customize your Arch Linux system identity")
    parser.add_argument("--name", help="Set the OS Name (e.g. MySuperOS)")
    parser.add_argument("--id", help=(
        "Set the OS ID (lowercase, e.g. mysuperos). "
        "If a file path is given, it is used as the custom logo for fastfetch/neofetch "
        "and ~/.config/fastfetch/ is auto-created if needed."
    ))
    parser.add_argument("--pretty-name", help="Set the Pretty Name (e.g. MySuperOS GNU/Linux)")
    parser.add_argument("--hostname", help="Set the system hostname")
    parser.add_argument("--color", help=(
        "Set the accent color for fastfetch/neofetch output. "
        "Accepts a color name (e.g. cyan, red, magenta) or an ANSI number (0-15)."
    ))
    parser.add_argument("--home-url", help="Set the Home Page URL")
    parser.add_argument("--reset", action="store_true", help="Restore /etc/os-release and /etc/hostname from backups")
    parser.add_argument("--install", help="Install a package automatically using the best tool (pacman, yay, curl script, etc.)")
    parser.add_argument("--interactive", action="store_true", help="Launch interactive setup menu to customize everything")
    parser.add_argument("--show-specs", action="store_true", help="Display summary of custom OS properties and hardware specifications")
    parser.add_argument("--lsb-release", action="store_true", help="Also generate /etc/lsb-release for Debian/Ubuntu compatibility layers")
    parser.add_argument("--docker-start", help="Start a Docker container from compose file path")
    parser.add_argument("--docker-stop", help="Stop a Docker container or service")
    parser.add_argument("--docker-rm", help="Stop and remove a Docker container or service")
    
    for service in SERVICE_DESCRIPTIONS:
        desc = SERVICE_DESCRIPTIONS.get(service, f"Setup {service}")
        parser.add_argument(f"--setup-{service}", action="store_true", help=f"Setup {service} Docker container - {desc}")

    args = parser.parse_args()

    if len(sys.argv) == 1:
        parser.print_help()
        sys.exit(0)

    if args.install:
        install_package(args.install)
        sys.exit(0)

    if args.show_specs or (len(sys.argv) == 2 and sys.argv[1] == "--show-specs"):
        show_specs()
        sys.exit(0)

    if args.interactive:
        run_interactive()
        sys.exit(0)

    # Handle docker commands BEFORE check_root() - they don't require sudo
    if args.docker_start:
        docker_compose_action(args.docker_start, "start")
        sys.exit(0)

    if args.docker_stop:
        docker_compose_action(args.docker_stop, "stop")
        sys.exit(0)

    if args.docker_rm:
        docker_compose_action(args.docker_rm, "rm")
        sys.exit(0)

    # Handle --setup-* arguments BEFORE check_root() - they don't require sudo
    for service in SERVICES:
        arg_name = f"setup_{service.replace('-', '_')}"
        if getattr(args, arg_name, False):
            SERVICES[service]()
            sys.exit(0)

    # --color and --id (logo path) both need to know the real user but NOT necessarily root
    # However writing /etc/os-release does. We'll get the user early for fetch updates.
    username, user_home = get_real_user()

    # Handle --color (no root needed for user config files)
    if args.color:
        if username and user_home:
            update_fetch_os_info(username, user_home, color=args.color)
        else:
            print("Warning: Could not determine the real user home directory. Skipping fetch color update.")

    # Everything below modifies system files and needs root
    check_root()

    if args.reset:
        restored = False
        for path in [OS_RELEASE_PATH, HOSTNAME_PATH]:
            bak = path + ".bak"
            if os.path.exists(bak):
                shutil.copy2(bak, path)
                print(f"Restored {path} from {bak}")
                restored = True
        if not restored:
            print("No backups found to restore.")
        sys.exit(0)

    data = parse_os_release()

    modified = False

    if args.name:
        data["NAME"] = args.name
        modified = True

    if args.id:
        expanded = os.path.expanduser(args.id)
        if os.path.exists(expanded):
            # It's a file/path — treat as custom logo
            logo_path = os.path.abspath(expanded)
            data["ID"] = "custom_os"
            data["LOGO"] = logo_path
            if username and user_home:
                update_fetch_logo(logo_path, username, user_home)
            else:
                print("Warning: Could not determine user home. Skipping fastfetch/neofetch logo update.")
        else:
            # Plain OS ID string — still ensure fastfetch config dir/file exists
            data["ID"] = args.id
            if username and user_home:
                ensure_fastfetch_config(username, user_home)
        modified = True

    if args.pretty_name:
        data["PRETTY_NAME"] = args.pretty_name
        modified = True

    if args.home_url:
        data["HOME_URL"] = args.home_url
        modified = True

    if modified:
        write_os_release(data)
        print("Updated /etc/os-release successfully!")

        # Update fastfetch/neofetch with new OS info system-wide
        if username and user_home:
            update_fetch_os_info(username, user_home, color=args.color)

        if args.lsb_release:
            name_val = data.get("NAME", "SpaceOS")
            id_val = data.get("ID", "spaceos")
            pretty_val = data.get("PRETTY_NAME", f"{name_val} GNU/Linux")
            write_lsb_release(name_val, id_val, pretty_val)

        if "PRETTY_NAME" in data:
            with open(ISSUE_PATH, "w") as f:
                f.write(f"{data['PRETTY_NAME']} (\\l)\n\n")

    if args.hostname:
        set_hostname(args.hostname)


if __name__ == "__main__":
    main()
